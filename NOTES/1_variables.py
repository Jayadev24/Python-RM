# Variables : is a name given to a "memory location"

name = "Jayadev"    # the string is stored in the memory location
age = 20

age2 = age  # "=" the assignment operator always stores the value in left into right

print("my name is ", name)   # when called the memory location returns the value stored inside it
print("my age is ", age2)

print(type(name))  # python automatically detects the datatype stored inside it